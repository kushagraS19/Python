# String & numeric values can operate together with * operator.

a,b = 2,4
txt = "k"

print(b*txt)

# String & string can operate together with + operator.

s1 = "ku"
s2 = "sh"

print((s1 + s2) + "agra")

# Numeric values can operate with all arithmetic operators.

a,b = 5,7
c = 5

print(" ")
print(a + b * c)

# Arithmetic expression with integer and float values will result in Float.

n,k = 3,4.9

print("")
print(n*k)

# Result of division operator with two integers will be float.

num1 = 1
num2 = 2

print("")
print(num1/num2)

